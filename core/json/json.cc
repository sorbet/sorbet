void JSON::symbolToJSON(rapidjson::PrettyWriter<rapidjson::StringBuffer> &writer, const GlobalState &gs, SymbolRef sym,
                        bool showFull) {
    writer.StartObject();

    // Fields are written in proto field number order to match proto3 JSON output.
    // id (field 1)
    auto id = sym.rawId();
    if (id != 0) {
        writer.Key("id");
        writer.Int(id);
    }

    // name (field 2) — always a message, proto3 omits if not set, but it's always set here
    writer.Key("name");
    nameToJSON(writer, gs, sym.name(gs));

    // kind (field 3)
    string_view kindStr;
    if (sym.isClassOrModule()) {
        kindStr = "CLASS_OR_MODULE"sv;
    } else if (sym.isStaticField(gs)) {
        kindStr = "STATIC_FIELD"sv;
    } else if (sym.isField(gs)) {
        kindStr = "FIELD"sv;
    } else if (sym.isMethod()) {
        kindStr = "METHOD"sv;
    } else if (sym.isTypeMember()) {
        kindStr = "TYPE_MEMBER"sv;
    } else if (sym.isTypeParameter()) {
        kindStr = "TYPE_ARGUMENT"sv;
    }
    if (!kindStr.empty()) {
        writer.Key("kind");
        writer.String(kindStr.data(), kindStr.size());
    }

    // superClass (field 6) — only for CLASS_OR_MODULE
    if (sym.isClassOrModule()) {
        auto klass = sym.asClassOrModuleRef();
        if (klass.data(gs)->superClass().exists()) {
            auto superClassId = SymbolRef(klass.data(gs)->superClass()).rawId();
            if (superClassId != 0) {
                writer.Key("superClass");
                writer.Int(superClassId);
            }
        }
    }

    // mixins (field 8) — only for CLASS_OR_MODULE
    if (sym.isClassOrModule()) {
        auto klass = sym.asClassOrModuleRef();
        auto mixins = klass.data(gs)->mixins();
        if (!mixins.empty()) {
            writer.Key("mixins");
            writer.StartArray();
            for (auto thing : mixins) {
                writer.Int(SymbolRef(thing).rawId());
            }
            writer.EndArray();
        }
    }

    // children (field 14) — for CLASS_OR_MODULE and METHOD
    if (sym.isClassOrModule()) {
        // Collect children first to check if array is non-empty
        vector<SymbolRef> children;
        for (auto pair : sym.asClassOrModuleRef().data(gs)->membersStableOrderSlow(gs)) {
            if (pair.first == Names::singleton() || pair.first == Names::attached() ||
                pair.first == Names::mixedInClassMethods() || pair.first == Names::Constants::AttachedClass()) {
                continue;
            }
            if (!pair.second.exists()) {
                continue;
            }
            if (!showFull && !pair.second.isPrintable(gs)) {
                continue;
            }
            children.emplace_back(pair.second);
        }
        if (!children.empty()) {
            writer.Key("children");
            writer.StartArray();
            for (auto child : children) {
                symbolToJSON(writer, gs, child, showFull);
            }
            writer.EndArray();
        }
    } else if (sym.isMethod()) {
        vector<SymbolRef> children;
        for (auto typeParam : sym.asMethodRef().data(gs)->typeParameters()) {
            if (!typeParam.exists()) {
                continue;
            }
            if (!showFull && !typeParam.data(gs)->isPrintable(gs)) {
                continue;
            }
            children.emplace_back(typeParam);
        }
        if (!children.empty()) {
            writer.Key("children");
            writer.StartArray();
            for (auto child : children) {
                symbolToJSON(writer, gs, child, showFull);
            }
            writer.EndArray();
        }
    }

    // aliasTo (field 16) — only for STATIC_FIELD with AliasType
    if (sym.isStaticField(gs)) {
        auto field = sym.asFieldRef();
        if (core::isa_type<core::AliasType>(field.data(gs)->resultType)) {
            auto type = core::cast_type_nonnull<AliasType>(field.data(gs)->resultType);
            auto aliasId = type.symbol.rawId();
            if (aliasId != 0) {
                writer.Key("aliasTo");
                writer.Int(aliasId);
            }
        }
    }

    // arguments (field 17) — only for METHOD
    if (sym.isMethod()) {
        auto &params = sym.asMethodRef().data(gs)->parameters;
        if (!params.empty()) {
            writer.Key("arguments");
            writer.StartArray();
            for (auto &param : params) {
                argumentToJSON(writer, gs, param);
            }
            writer.EndArray();
        }
    }

    writer.EndObject();
}

// ----- Metrics JSON serialization ---------------------------------------------

string JSON::metricsToJSON(const CounterState &counters, string_view prefix, string_view repo, string_view branch,
                           string_view sha, string_view status) {
    rapidjson::StringBuffer buffer;
    rapidjson::PrettyWriter<rapidjson::StringBuffer> writer(buffer);
    writer.SetIndent(' ', 1);

    writer.StartObject();

    // Fields in proto field number order: repo(1), sha(2), status(3), branch(4), timestamp(5), uuid(6), metrics(10)
    if (!repo.empty()) {
        writer.Key("repo");
        writer.String(repo.data(), repo.size());
    }

    if (!sha.empty()) {
        writer.Key("sha");
        writer.String(sha.data(), sha.size());
    }

    if (!status.empty()) {
        writer.Key("status");
        writer.String(status.data(), status.size());
    }

    if (!branch.empty()) {
        writer.Key("branch");
        writer.String(branch.data(), branch.size());
    }

    // Proto3 serializes int64 as a string in JSON.
    auto unix_timestamp = chrono::seconds(time(nullptr));
    if (unix_timestamp.count() != 0) {
        writer.Key("timestamp");
        auto ts = to_string(unix_timestamp.count());
        writer.String(ts.data(), ts.size());
    }

    // UUID version 1 as specified in RFC 4122
    string uuid = fmt::format("{:#08x}-{:#04x}-{:#04x}-{:#04x}-{:#08x}{:#04x}", (unsigned long long)Random::uniformU8(),
                              Random::uniformU4(), Random::uniformU4(0, 0x0fff) | 0x4000,
                              Random::uniformU4(0, 0x3fff) | 0x8000, (unsigned long long)Random::uniformU8(), rand());
    if (!uuid.empty()) {
        writer.Key("uuid");
        writer.String(uuid.data(), uuid.size());
    }

    counters.counters->canonicalize();

    // Collect all metric entries
    bool hasMetrics = false;
    auto startMetrics = [&]() {
        if (!hasMetrics) {
            writer.Key("metrics");
            writer.StartArray();
            hasMetrics = true;
        }
    };

    auto writeMetric = [&](string_view name, double value) {
        startMetrics();
        writer.StartObject();
        writer.Key("name");
        writer.String(name.data(), name.size());
        writer.Key("value");
        // Proto3 JSON serializes double values that are exact integers without a decimal point.
        // rapidjson's Double() always includes a decimal, so we use Int64() for integral values.
        if (value == static_cast<int64_t>(value)) {
            writer.Int64(static_cast<int64_t>(value));
        } else {
            writer.Double(value);
        }
        writer.EndObject();
    };

    for (auto &cat : counters.counters->countersByCategory) {
        CounterImpl::CounterType sum = 0;
        for (auto &e : cat.second) {
            sum += e.second;
            writeMetric(absl::StrCat(prefix, ".", cat.first, ".", e.first), e.second);
        }
        writeMetric(absl::StrCat(prefix, ".", cat.first, ".total"), sum);
    }

    for (auto &hist : counters.counters->histograms) {
        CounterImpl::CounterType sum = 0;
        int histMin = hist.second.begin()->first;
        int histMax = hist.second.begin()->first;
        for (auto &e : hist.second) {
            sum += e.second;
            histMin = min(histMin, e.first);
            histMax = max(histMin, e.first);
        }
        CounterImpl::CounterType running = 0;
        vector<pair<int, bool>> percentiles = {{25, false}, {50, false}, {75, false}, {90, false}};
        for (auto &e : hist.second) {
            running += e.second;
            for (auto &pct : percentiles) {
                if (pct.second) {
                    continue;
                }
                if (running >= sum * pct.first / 100) {
                    pct.second = true;
                    writeMetric(absl::StrCat(prefix, ".", hist.first, ".p", pct.first), e.first);
                }
            }
        }
        writeMetric(absl::StrCat(prefix, ".", hist.first, ".min"), histMin);
        writeMetric(absl::StrCat(prefix, ".", hist.first, ".max"), histMax);
        writeMetric(absl::StrCat(prefix, ".", hist.first, ".total"), sum);
    }

    for (auto &e : counters.counters->counters) {
        writeMetric(absl::StrCat(prefix, ".", e.first), e.second);
    }

    if (hasMetrics) {
        writer.EndArray();
    }

    writer.EndObject();

    string result(buffer.GetString(), buffer.GetLength());
    result.push_back('\n');
    return result;
}

